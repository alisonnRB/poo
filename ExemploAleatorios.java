

import java.util.Random;

class ExemploAleatorios{

	public static void main(String args[]) {
		
		Random r = new Random();
		
		// gerando um numero float
		
		System.out.printf("float aleatório é %f \n", r.nextFloat() );
		
		System.out.printf("boolean aleatório %s \n", r.nextBoolean() );
		
	
	}

}
